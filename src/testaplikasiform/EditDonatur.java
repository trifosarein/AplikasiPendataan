    private void tabel_donatur(){
        DefaultTableModel t = new DefaultTableModel();
        t.addColumn("Nama");
        t.addColumn("Telepon");
        donatur_TBL.setModel(t);
        try{
            res=stat.executeQuery("select * from tb_donatur order by nama_donatur asc");
            while(res.next()){
                t.addRow(new Object[]{
                    res.getString("nama_donatur"),
                    res.getString("telp_donatur"),
                });
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

