    private void donatur_TBLMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_donatur_TBLMouseClicked
        // TODO add your handling code here:
        int i = donatur_TBL.getSelectedRow();
        if(i==-1){
            return;
        }
        String code = (String)donatur_TBL.getValueAt(i,0);
        String code1 = (String)donatur_TBL.getValueAt(i,1);
        String code2 = "";
        String code3 = "";
        String code4 = "";
        try{
            res=stat.executeQuery("select * from tb_donatur where nama_donatur='"+code+"' AND telp_donatur='"+code1+"'");
            while(res.next()){
                code2 = res.getString("id_donatur");
                code3 = res.getString("profesi_donatur");
                code4 = res.getString("alamat_donatur");
                
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
        namaDonatur_TF.setText(code);
        idDonatur_TF.setText(code2);
        profesi_TF.setText(code3);
        nohp_TF.setText(code1);
        alamat_TA.setText(code4);
        alamat_TA.setForeground(Color.BLACK);
    }//GEN-LAST:event_donatur_TBLMouseClicked

