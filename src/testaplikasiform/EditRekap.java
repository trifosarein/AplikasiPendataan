    private void tabel_rekap(){
        DefaultTableModel t = new DefaultTableModel();
        t.addColumn("ID");
        t.addColumn("Tanggal");
        t.addColumn("Nama Donatur");
        t.addColumn("Jenis Transaksi");
        t.addColumn("Bank");
        t.addColumn("Nama Relawan");
        t.addColumn("Jumlah Donasi");
        t.addColumn("Kegiatan");
        rekap_TBL.setModel(t);
        try{
            res=stat.executeQuery("select * from tb_rekap inner join tb_donatur on tb_rekap.id_donatur=tb_donatur.id_donatur inner join tb_relawan on tb_rekap.id_relawan=tb_relawan.id_relawan order by tgl_rekap, id_rekap asc");
            while(res.next()){
                t.addRow(new Object[]{
                    res.getString("id_rekap"),
                    res.getDate("tgl_rekap"),
                    res.getString("nama_donatur"),
                    res.getString("jenis_transaksi"),
                    res.getString("nama_bank"),
                    res.getString("nama_relawan"),
                    res.getString("jumlah_donasi"),
                    res.getString("kegiatan"),
                });
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

