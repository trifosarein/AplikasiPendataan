    private void edit_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_BTNActionPerformed
        // TODO add your handling code here:
        String keterangan = "";
        String namabank = "";
        String idDonatur = "";
        String idRelawan = "";
        String jumlahDonasi = "";
        String kegiatan = "";
        switch (keterangan_TA.getText()){
            case "Maks 300 huruf":
                keterangan = "";
                break;
            default:
                keterangan = keterangan_TA.getText();
                break;
        }
        switch (namaBank_CB.getSelectedIndex()){
            case -1:
                namabank = "";
                break;
            default:
                namabank = (String) namaBank_CB.getSelectedItem();
                break;
        }
        /////
        switch (idDonatur_TF.getText()){
            case "":
                JOptionPane.showMessageDialog(null, "Kolom Nama / ID Donatur masih kosong!");
                break;
            default:
                idDonatur = idDonatur_TF.getText();
                break;
        }
        switch (idRelawan_TF.getText()){
            case "":
                JOptionPane.showMessageDialog(null, "Kolom Nama / ID Relawan masih kosong!");
                break;
            default:
                idRelawan = idRelawan_TF.getText();
                break;
        }
        switch (jumlahDonasi_TF.getText()){
            case "":
                JOptionPane.showMessageDialog(null, "Kolom Jumlah Donasi masih kosong!");
                break;
            default:
                jumlahDonasi = jumlahDonasi_TF.getText();
                break;
        }
        switch (kegiatan_CB.getSelectedIndex()){
            case -1:
                JOptionPane.showMessageDialog(null, "Kolom Kegiatan masih kosong!");
                break;
            default:
                kegiatan = (String) kegiatan_CB.getSelectedItem();
                break;
        }
        if(tanggal_DC.getDate()==null){
            JOptionPane.showMessageDialog(null, "Kalendar masih kosong!");
        }else{
            java.sql.Date date = new java.sql.Date(tanggal_DC.getDate().getTime());
            if(namabank==""&&namaBank_CB.isEnabled()){
                JOptionPane.showMessageDialog(null, "Data bank kosong!");
            }else{
                if(idDonatur == "" || idRelawan == "" || jumlahDonasi == "" || kegiatan == ""){
                    JOptionPane.showMessageDialog(null, "Data tidak boleh ada yang kosong!");
                }else{
                    int ok=JOptionPane.showConfirmDialog(null,"Apakah anda yakin untuk edit data ini?","Confirmation",JOptionPane.YES_NO_OPTION);
                    try{
                        String sql="update tb_rekap set tgl_rekap=?,id_donatur=?,jenis_transaksi=?,nama_bank=?,id_relawan=?,jumlah_donasi=?,kegiatan=?,keterangan=? where id_rekap='"+idRekap_TF.getText()+"'";
                        PreparedStatement st=con.prepareStatement(sql);
                        if(ok==0){
                            try{
                                st.setDate(1, date);
                                st.setString(2, idDonatur);
                                st.setString(3, (String) cashTransfer_CB.getSelectedItem());
                                st.setString(4, namabank);
                                st.setString(5, idRelawan);
                                st.setString(6, jumlahDonasi);
                                st.setString(7, kegiatan);
                                st.setString(8, keterangan);
                                st.executeUpdate();
                                JOptionPane.showMessageDialog(null,"Update Data Berhasil");
                            } catch(Exception e){
                                JOptionPane.showMessageDialog(null, "Update Data Gagal");
                            }
                        }
                    } catch (Exception e){}
                }
            }
        }
        tabel_rekap();
    }//GEN-LAST:event_edit_BTNActionPerformed

    private void delete_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_BTNActionPerformed
        // TODO add your handling code here:
        switch(idRekap_TF.getText()){
            case "":
                JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus terlebih dahulu.");
                break;
            default:
                int ok=JOptionPane.showConfirmDialog(null, "Apakah anda yakin untuk hapus data ini?", "Confirmation", JOptionPane.YES_NO_OPTION);
                if(ok==0){
                    try{
                        String sql="delete from tb_rekap where id_rekap='"+idRekap_TF.getText()+"'";
                        PreparedStatement st=con.prepareStatement(sql);
                        st.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Delete Data Sukses");
                    } catch(Exception e){
                        JOptionPane.showMessageDialog(null, "Delete Data Gagal");
                    }
                    Clear();
                }
            break;
        }
        tabel_rekap();
    }//GEN-LAST:event_delete_BTNActionPerformed

